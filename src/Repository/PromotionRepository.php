    // MÃ©thode pour obtenir les promotions actives
    public function findActivePromotions()
    {
        $now = new \DateTime();

        return $this->createQueryBuilder('p')
            ->where('p.date_deb <= :now')
            ->andWhere('p.date_fin >= :now')
            ->setParameter('now', $now)
            ->getQuery()
            ->getResult();
    }


